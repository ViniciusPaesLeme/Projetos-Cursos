"""
Exercício 

Desenvolva um algoritmo que converta uma temperatura em Celsius (C) para Fahrenheit (F). A equação de conversão é: 

F = (9 * C /5) +32

"""

c = float(input('Digite uma temperatura em Celsius: '))
f = (9 * c /5) +32

print(f'Celsius: {c:.2f},49 é igual a Fahrenheit: {f:.2f}')